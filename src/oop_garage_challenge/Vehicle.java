package oop_garage_challenge;

public class Vehicle {

	// fields
	
//	public static final String Iterator = null;
	String name;
	String IDNumber;
	String vehicleType;
	int numberOfWheels;
	
	
	public Vehicle(String name, String IDNumber, String vehicleType, int numberOfWheels) {
		super();
		this.name = name;
		this.IDNumber = IDNumber;
		this.vehicleType = vehicleType;
		this.numberOfWheels = numberOfWheels;
	}

	public String getIDNumber() {
		return IDNumber;
	}
	
	public String getName() {
		return name;
	}
	
	
	
	
	}
	
	
	
	
	





